a = 11
b = 12
# a = int('1011', 2)           Taking Binary Input and then performing Bitwise Operations
# b = int('1100', 2)  
print("a=",a)
print("b=",b)
print("Binary of a:", bin(a))
print("Binary of b:", bin(b))
print("Bitwise AND:", a&b)
print("Bitwise OR:", a|b)
print("Bitwise XOR:", a^b)
print("Bitwise Left Shift of 11:", a<<1)     # ans = a * 2**b
print("Bitwise Right Shift of 11:", a>>1)    # ans = a / 2**b